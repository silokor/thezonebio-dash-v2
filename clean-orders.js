// Excel serial date to YYYY-MM-DD HH:MM:SS
function excelToDate(serial) {
  if (typeof serial === 'string' && serial.includes('-')) {
    // Already a date string
    if (serial.includes(' ')) return serial;
    return serial + ' 00:00:00';
  }
  const num = parseFloat(serial);
  if (isNaN(num)) return serial;
  
  // Excel epoch is 1899-12-30
  const epoch = new Date(1899, 11, 30);
  const days = Math.floor(num);
  const fraction = num - days;
  
  const date = new Date(epoch.getTime() + days * 86400000);
  const hours = Math.floor(fraction * 24);
  const minutes = Math.floor((fraction * 24 - hours) * 60);
  const seconds = Math.floor(((fraction * 24 - hours) * 60 - minutes) * 60);
  
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  const hh = String(hours).padStart(2, '0');
  const mm = String(minutes).padStart(2, '0');
  const ss = String(seconds).padStart(2, '0');
  
  return `${y}-${m}-${d} ${hh}:${mm}:${ss}`;
}

